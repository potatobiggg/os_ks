#ifndef FORMAT_H
#define FORMAT_H

/*
 * ============================================================
 * format.h / format.c —— 文件系统格式化模块
 * ============================================================
 *
 * 作用：提供 fs_format() 函数，从零初始化整个文件系统。
 *
 * 触发场景：
 *   1. 宿主文件不存在时（首次启动），main.c 自动调用 fs_format()
 *   2. 用户在 shell 中输入 "format" 命令手动格式化
 *
 * 格式化流程概述：
 *   - 创建宿主文件（如 filesystem.dat）
 *   - 所有块清零（TOTAL_BLOCKS × 512 字节）
 *   - 初始化超级块（区域大小、起止地址等元信息）
 *   - 构建空闲 inode 栈（inode 2 ~ TOTAL_INODES）
 *   - 构建空闲数据块栈（成组链接法 / Group Linking）
 *   - 创建根目录（inode 1，含 "." 和 ".." 两个目录项）
 *   - 超级块写回磁盘（块 0）
 *
 * 注意：
 *   - 格式化会清除所有数据，不可恢复！
 *   - 用户目录（如 /usr1 等）由 user_init() 在格式化后创建
 */

/**
 * fs_format — 格式化（初始化）整个文件系统
 *
 * 首先以 "wb+" 模式创建/覆盖宿主文件，清空原有内容。
 * 然后将全部磁盘块写零，构造超级块元数据，初始化空闲
 * inode 和数据块栈，最后创建根目录并将超级块写回磁盘。
 *
 * 返回值：
 *   0  — 格式化成功
 *  -1  — 格式化失败（无法创建宿主文件、无空闲块等）
 */
int fs_format(void);

#endif /* FORMAT_H */
